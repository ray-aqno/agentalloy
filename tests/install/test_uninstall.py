class TestMcpCleanup:
    def test_cursor_mcp_entry_removed(self, repo_root: Path) -> None:
        cursor = repo_root / ".cursor" / "mcp.json"
        cursor.parent.mkdir(parents=True)
        cursor.write_text(
            json.dumps({"mcpServers": {"skillsmith": {"command": "x"}, "other": {"command": "y"}}})
        )
        uninstall(root=repo_root)
        cfg = json.loads(cursor.read_text())
        assert "skillsmith" not in cfg["mcpServers"]
        assert "other" in cfg["mcpServers"]

    def test_cursor_mcp_file_deleted_when_empty(self, repo_root: Path) -> None:
        cursor = repo_root / ".cursor" / "mcp.json"
        cursor.parent.mkdir(parents=True)
        cursor.write_text(json.dumps({"mcpServers": {"skillsmith": {"command": "x"}}}))
        uninstall(root=repo_root)
        assert not cursor.exists()

    def test_continuerc_mcp_servers_removed(self, repo_root: Path) -> None:
        config: dict[str, Any] = {
            "models": [{"title": "GPT-4"}],
            "mcpServers": {"skillsmith": {"command": "x"}, "other": {"command": "y"}},
        }
        (repo_root / ".continuerc.json").write_text(json.dumps(config))
        uninstall(root=repo_root)
        cfg = json.loads((repo_root / ".continuerc.json").read_text())
        assert "skillsmith" not in cfg.get("mcpServers", {})
        assert "other" in cfg["mcpServers"]


